package club.spreadme.util.logger;

import club.spreadme.lang.Reflection;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.BeansException;
import org.springframework.beans.factory.config.BeanPostProcessor;
import org.springframework.stereotype.Component;

@Component
public class LoggerComponent implements BeanPostProcessor {

    @Override
    public Object postProcessBeforeInitialization(Object bean, String beanName) throws BeansException {
        return bean;
    }

    @Override
    public Object postProcessAfterInitialization(Object bean, String beanName) throws BeansException {

        Reflection.doWithField(bean.getClass(),
                field -> Reflection.setFieldValue(field, bean, LoggerFactory.getLogger(bean.getClass())),
                field -> field.isAnnotationPresent(Logable.class) && Logger.class.equals(field.getType()));

        return bean;
    }
}
